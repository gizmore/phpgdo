	/**
	 * Get primary key column names.
	 *
	 * @return string[]
	 */
	public function gdoPrimaryKeyColumnNames(): array
	{
		$table = self::table();
		$cache = $table->cache;
		if (isset($cache->pkNames))
		{
			return $cache->pkNames;
		}
		$names = [];
		foreach ($this->gdoColumnsCache() as $column)
		{
			if ($column->isPrimary())
			{
				$names[] = $column->getName();
			}
			else
			{
				break; # Assume PKs are first until no more PKs (@XXX one of the quirky conventions!)
			}
		}

		if (empty($names))
		{
			foreach ($this->gdoColumnsCache() as $gdt)
			{
				if ($name = $gdt->getName())
				{
					$names[] = $name;
				}
			}
		}

		$cache->pkNames = $names;

		return $names;
	}

