		$values = [];
		foreach ($this->gdoColumnsCache() as $gdt)
		{
			if ($gdt->isSerializable())
			{
				$data = $gdt->gdo($this)->getGDOData();
				foreach ($data as $k => $v)
				{
					if ($v !== null)
					{
						$values[$k] = $v;
					}
				}
			}
		}
		return $values;
	}

